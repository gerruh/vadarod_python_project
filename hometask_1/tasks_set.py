'''
На вход программа принимает 2 строки и выводит их общие символы.
'''
str_input_1 = set(input('Введите первую строку: '))
str_input_2 = set(input('Введите вторую строку: '))

print(f'Общие символы в {str_input_1} и {str_input_2}: {str_input_1 & str_input_2}')

