# 9.3
'''
Написать задачу со множеством.
Представление: условия и решение.

Обновление множества: если второе множество - не сабсет первого, добавить его к первому
'''
set_1 = set(input())
set_2 = set(input())

if not set_2.issubset(set_1):
    print(f'set_1 будет обновлен {set_2}, был: {set_1}')
    set_1.update(set_2)
    print(f'Обновленный set_1: {set_1}')
else:
    print(f'{set_2} уже есть в {set_1}')