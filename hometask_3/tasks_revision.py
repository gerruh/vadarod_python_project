'''
Введите строку c клавиатуры, которая состоит из букв разных регистров. Нужно очистить эту строку от всех заглавных букв и вывести результат на экран.
'''
input_str = input('Введите строку состоящую из букв разного регистра: ')
output_str = ''.join([x for x in input_str if not x.isupper()])

print(f'Строка {input_str} без заглавных букв: {output_str}')